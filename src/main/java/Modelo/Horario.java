package Modelo;

import java.util.Date;

/**
 *
 * @author Omar Tc
 */
public class Horario {
    
    String DNI;
    Date fecha;
    String turno;

    public String getDNI() {
        return DNI;
    }

    public void setDNI(String DNI) {
        this.DNI = DNI;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

    public String getTurno() {
        return turno;
    }

    public void setTurno(String turno) {
        this.turno = turno;
    }
    
    
    
    
    
    
}
